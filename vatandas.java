
public class vatandas {
   private String ad;
   private String soyad;
   private String tckn;

    public vatandas(String ad, String soyad, String tckn) {
        this.ad = ad;
        this.soyad = soyad;
        this.tckn = tckn;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public String getSoyad() {
        return soyad;
    }

    public void setSoyad(String soyad) {
        this.soyad = soyad;
    }

    public String getTckn() {
        return tckn;
    }

    public void setTckn(String tckn) {
        this.tckn = tckn;
    }
   
   
   
}
